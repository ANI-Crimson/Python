# AND (&)
resultado0= True & True # Devuelve True.
resultado1= False & True # Devuelve False.
resultado2= True & False # Devuelve False.
resultado3= False & False # Devuelve True.

# OR (|)
resultado4= True | True # Devuelve False.
resultado5= False | True # Devuelve True.
resultado6= True | False # Devuelve True.
resultado7= False | False # Devuelve False.

# NOT (not)
resultado8= not True # Devuelve False.
resultado9= not False # Devuelve True.


